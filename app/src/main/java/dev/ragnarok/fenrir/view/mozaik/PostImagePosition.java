package dev.ragnarok.fenrir.view.mozaik;

import androidx.annotation.NonNull;

public class PostImagePosition {

    public int sizeX;
    public int sizeY;
    public int marginX;
    public int marginY;

    @NonNull
    @Override
    public String toString() {
        return "PostImagePosition{" +
                "sizeX=" + sizeX +
                ", sizeY=" + sizeY +
                ", marginX=" + marginX +
                ", marginY=" + marginY +
                '}';
    }
}
